    public boolean getRecordValidation() throws SQLException{
        
        
        String DateAchat = sdf.format(txtDateAchat.getDate());
        
        
            
        
        if(controller.getValidation().isEmpty(txtCodeBare.getText().trim())){
            JOptionPane.showMessageDialog(rootPane, "entrer un code bare valide ");
            txtCodeBare.requestFocus();
            return false;
        }else if(controller.getValidation().isExist(txtCodeBare.getText().trim(),"exemplaire","codeBar")){
            JOptionPane.showMessageDialog(rootPane, "entrer un bar code unique ");
            txtPrix.requestFocus();
            return false;
        }

        else if(controller.getValidation().isEmpty(txtPrix.getText().trim())){
            JOptionPane.showMessageDialog(rootPane, "entrer un prix valide ");
            txtPrix.requestFocus();
            return false;
        }
        
                              
        
        else if(controller.getValidation().isEmpty(DateAchat) ){
            JOptionPane.showMessageDialog(rootPane, "entrer une date valide ");
            txtDateAchat.requestFocus();
            return false; 
        }
         
        else if(controller.getValidation().isEmpty(txtIDDocument.getText().trim())){
            JOptionPane.showMessageDialog(rootPane, "entrer un ID Document valide ");
            txtIDDocument.requestFocus();
            return false;
        }
        else if(!controller.getValidation().isExist(txtIDDocument.getText().trim(), "doc" , "idDoc")){
            JOptionPane.showMessageDialog(rootPane, "cet ID Doc n'existe pas");
            txtIDDocument.requestFocus();
            return false;
        }
        
        return true;
    }
      
    
    
