    private void btnUpdate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdate1ActionPerformed
   
        try {
            if(getRecordValidation()== true){
                String IdEx=txtIDExemplaire.getText().trim();
                String CodeB=txtCodeBare.getText().trim();
                String Pr=txtPrix.getText().trim();
                String DateA=sdf.format(txtDateAchat.getDate());
                String IdD=txtIDDocument.getText().trim();
                
                controller.getExemplaire().setIdExemplaire(Integer.parseInt(IdEx));
                controller.getExemplaire().setCodeBar(CodeB);
                controller.getExemplaire().setPrix(Double.parseDouble(Pr));
                controller.getExemplaire().setDateAchat(DateA);
                controller.getExemplaire().setIdDoc(Integer.parseInt(IdD));
                
                try {
                    controller.getDao().update(controller.getExemplaire());
                    
                    JOptionPane.showMessageDialog(rootPane,"update succesful ");
                    initTable();
                    
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, ex);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(ExemplaireView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdate1ActionPerformed

